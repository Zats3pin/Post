package com.eltex;

public class Attachment {

    String url = "null";

    String type = "null";

    public void setUrl(String url) {
        this.url = url;
    }

    public void setType(String type) {
        this.type = type;
    }
    public String getUrl() {
        return url;
    }

    public String getType() {
        return type;
    }


}
